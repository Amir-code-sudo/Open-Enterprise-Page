// Counter animation for shares
function animateCounter(element, target, duration = 2000) {
    let start = 0;
    const increment = target / (duration / 16);
    
    const counter = setInterval(() => {
        start += increment;
        element.textContent = Math.floor(start);
        
        if (start >= target) {
            element.textContent = target;
            clearInterval(counter);
        }
    }, 16);
}

// Animate counters when they come into view
const counterObserver = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
        if (entry.isIntersecting) {
            const shareElements = entry.target.querySelectorAll('.member span');
            shareElements.forEach(span => {
                if (span.textContent.includes('SHARES')) {
                    const number = parseInt(span.textContent.match(/\d+/)[0]);
                    const textPart = span.textContent.replace(/\d+/, '');
                    span.textContent = '0' + textPart;
                    animateCounter(span, number);
                }
            });
            counterObserver.unobserve(entry.target);
        }
    });
});

const membersSection = document.querySelector('.members-grid');
if (membersSection) {
    counterObserver.observe(membersSection);
}

// Add loading animation
window.addEventListener('load', () => {
    document.body.classList.add('loaded');
    
    // Animate hero content
    const heroContent = document.querySelector('.hero-content');
    const heroImage = document.querySelector('.hero-image');
    
    if (heroContent && heroImage) {
        heroContent.style.opacity = '0';
        heroContent.style.transform = 'translateX(-50px)';
        heroImage.style.opacity = '0';
        heroImage.style.transform = 'translateX(50px)';
        
        setTimeout(() => {
            heroContent.style.transition = 'all 0.8s ease';
            heroImage.style.transition = 'all 0.8s ease';
            heroContent.style.opacity = '1';
            heroContent.style.transform = 'translateX(0)';
            heroImage.style.opacity = '1';
            heroImage.style.transform = 'translateX(0)';
        }, 200);
    }
});

